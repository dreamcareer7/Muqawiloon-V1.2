require "rails_helper"

RSpec.feature "User requests a callback" do
  scenario "They view a business and click the callback button. They confirm their number and the business receives a notification"
    #business = FactoryGirl.create(:business)

    #user_logs_in

    #visit(businesses_path(city: City.first))

    #find(:xpath, "//a[@href='/businesses/#{business.id}']").click

    #find(:xpath, "//a[@href='/business/user_callbacks/new?business_id=#{business.id}']").click

    #click_button "Confirm number"

    #expect(business.incoming_notifications.count).to eq(1)

end
